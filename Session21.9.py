# This module provides access to the mathematical functions

import math

print("Value of pi is:", math.pi)
print("Value of e is:", math.e)    # exponential

print("Square Root of 4 is:", math.sqrt(4))
print("Factorial 5 is:", math.factorial(5))
print("log 2 is:", math.log(2))


print("log 5 is:", math.log2(5))   # log with base 2
print("log 5 is:", math.log(5))    # natural log to base e
print("log 5 is:", math.log10(5))   # log with base 10

# gives the floor value i.e. it remove numbers decimal point
print("Floor value of 100.12:", math.floor(100.12))

# it gives the approx value by adding 1 to the number
print("ceiling of 100.12:", math.ceil(100.12))

print("2 raise to the power 5:", math.pow(2,5))


